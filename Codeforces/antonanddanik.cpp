#include <bits/stdc++.h>

using namespace std;

int main(){
  int n, a, d;
  cin >> n;
  string s;
  cin >> s;
  a = 0;
  d = 0;
  for (int i = 0; i < n; i++){
    if (s[i] == 'A'){
      a++;
    } else {
      d++;
    }
  }
  if (d > a){
    cout << "Danik";
  } else if (a > d){
    cout << "Anton";
  } else {
    cout << "Friendship";
  }
}
