#include <bits/stdc++.h>

using namespace std;

int main(){
    int n;
    cin >> n;
    int h[n];
    int g[n];
    int s = 0;
    for (int i = 0; i < n; i++){
        cin >> h[i] >> g[i];
    }
    for (int x = 0; x < n; x++){
        for (int y = 0; y < n; y++){
            if (h[x] == g[y]){
                s++;
            }
        }
    }
    cout << s;
}
